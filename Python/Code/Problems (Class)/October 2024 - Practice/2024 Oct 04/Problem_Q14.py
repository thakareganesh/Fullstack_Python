# Question 14: Write a program to perform swapping 2 numbers without using 3rd variable and wihout using +,- operators.

a = 10
b = 20
print("Before Swapping values:")
print("a :", a)
print("b :", b)


a , b = b , a # --> swapping of values without third variable and any other operator

print("After Swapping Values:")
print("a :", a)
print("b :", b)