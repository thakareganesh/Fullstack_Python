# Plus (+) and Multiply (*) operators for string

# Example 1
fname = "Ganesh"
lname = "Thakare"
print(fname, lname + "10") # --> O/P: Ganesh Thakare10

#Example 2
print(fname * 5) # --> O/P: GaneshGaneshGaneshGaneshGanesh

# Example 3
print(5 * lname) # --> O/P: ThakareThakareThakareThakareThakare

# Example 4
# print("Ganesh" * "Thakare") # It will raise TypeError

#Example 5
print("#" * 15)
print("Ganesh Thakare")
