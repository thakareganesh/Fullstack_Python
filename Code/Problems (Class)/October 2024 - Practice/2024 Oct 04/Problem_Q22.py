# Question 22: Consider product cost is 200, gst is 20%. Now find the gst amount and final cost of the product.

product_cost = 200
gst_percent = 20
gst_amt = product_cost * gst_percent / 100
print("Product cost:",product_cost)
print("GST amount for product:", gst_amt) # --> gst ammount is 40.0
final_amt = product_cost + gst_amt
print("Final cost of the product:", final_amt) # --> final costs is 240.0