# Question 29: Consider biryani cost is 395.25, Read quantity from user then find Total Bill Amount.

biryani_cost = 395.25
quantity = int(input("How many Pizza's  do you want: "))

print("Biryani Price:",biryani_cost)
print("Ordered Biryani in quantity:",quantity)

total_bill = biryani_cost * quantity
print("Total Bill is:",total_bill)