"""
Question 7: Ms. Laxmi Opened swiggy observed that pizza cost is 345.60, She ordered 3 Pizza's.
Write a Program to calculate her bill amount. Swiggy added 20 rs as service charge and 30 rs as tip.
So find total bill amount.
"""

pizza_Cost = 345.60
ordered_Quantity = 3
service_Charge = 20
tip = 30
total_Bill_Amt = (pizza_Cost * ordered_Quantity) + service_Charge + tip
print("Total bill amount is:",total_Bill_Amt) # --> Output: 1086.80
